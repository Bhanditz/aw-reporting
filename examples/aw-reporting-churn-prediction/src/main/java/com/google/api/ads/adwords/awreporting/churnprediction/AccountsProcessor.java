package com.google.api.ads.adwords.awreporting.churnprediction;

import com.google.api.ads.adwords.awreporting.churnprediction.db.DatabaseReader;
import com.google.api.ads.adwords.awreporting.churnprediction.entities.AccountSignals;
import com.google.api.ads.adwords.awreporting.churnprediction.entities.AccountStatus;
import com.google.api.ads.adwords.awreporting.churnprediction.signals.SignalProcessor;

import org.joda.time.DateTime;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Component;

import java.util.List;

/**
 * TODO: Insert description here. (generated by gustavomoreira)
 */
@Component
public class AccountsProcessor {
  @Autowired
  private DatabaseReader databaseReader;

  @Autowired
  private SignalProcessor signalProcessor;

  public void createAccountsSignals(DateTime day) throws Exception {
    DateTime startDate = day;
    DateTime endDate = startDate.minusMonths(12);
    List<AccountStatus> accountsStatus = this.databaseReader.listAccountsStatus();

    for (AccountStatus accountStatus : accountsStatus) {
      AccountSignals accountSignals = this.signalProcessor.createSignalsForAccount(
          accountStatus.getAccountId(), startDate, endDate);

      this.databaseReader.saveSignals(accountSignals);
    }
  }
}
